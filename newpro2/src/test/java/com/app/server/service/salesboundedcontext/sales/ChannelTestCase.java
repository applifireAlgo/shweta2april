    private void validateChannel(EntityTestCriteria contraints, Channel channel) throws SpartanIncorrectDataException, SpartanConstraintViolationException, SpartanPersistenceException {
        if (contraints.getRuleType() == MIN_MAX) {
            channel.isValid();
        } else if (contraints.getRuleType() == NOT_NULL) {
            channel.isValid();
        } else if (contraints.getRuleType() == REGEX) {
            channel.isValid();
        } else if (contraints.getRuleType() == UNIQUE) {
            channelRepository.save(channel);
        }
    }

    private List<EntityTestCriteria> addingListOfFieldForNegativeTesting() {
        List<EntityTestCriteria> entityContraints = new java.util.ArrayList<EntityTestCriteria>();
        entityContraints.add(new EntityTestCriteria(NOT_NULL, 1, "channel", null));
        entityContraints.add(new EntityTestCriteria(MIN_MAX, 2, "channel", "TfZpg0AMTBYmihBa3zpZMAQWGp0aO9Lx6D6twbf1Ey9PST3QQDfuenTsXMMzjWgHg"));
        return entityContraints;
    }

